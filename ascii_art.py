from pyfiglet import figlet_format
from rich.text import Text

def timer_ascii(minutes, seconds, color="bold green"):
    time_str = f"{minutes:02d}:{seconds:02d}"
    time_ascii = figlet_format(time_str, font="big")
    return Text(time_ascii, style=color)


